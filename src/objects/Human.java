package objects;

// concrete class
public class Human {
	private int age;
	private int id;
	private String name;
	private int grades;
	
	// Age
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	// id
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
	// name
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	// grades
	public int getGrades() {
		return grades;
	}
	public void setGrades(int grades) {
		this.grades = grades;
	}	
	
}
