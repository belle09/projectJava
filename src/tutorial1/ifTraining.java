package tutorial1;

public class ifTraining {

	public static void main(String[] args) {

		int sum = 20;
		
		if(sum < 10){
			System.out.println("Yes, it's true");
		} else if(sum > 30) {
			System.out.println("No, it's false");
		} else {
			System.out.println("None of the above");
		} // end if
		
	} // end main

} // end class
